# Postcodes genuinely split between two or three operators at street
# level: the user can't be assigned automatically because the answer
# depends on which street/parish of the postcode they live on. The
# config flow shows a small chooser populated with this tuple; the
# first entry is what the legacy range-based resolver would have
# picked (so the behaviour is backwards-compatible if a caller
# accidentally goes through ``resolve`` instead of
# ``resolve_candidates``).
_SPLIT_POSTCODES: dict[str, tuple[str, ...]] = {
    "1770": ("de_watergroep", "farys"),  # Liedekerke
    "8020": ("farys", "de_watergroep"),  # Oostkamp
    "8400": ("farys", "de_watergroep"),  # Oostende (Stene/Mariakerke)
    "8450": ("aquaduin", "de_watergroep"),  # Bredene
    "8490": ("farys", "de_watergroep"),  # Jabbeke
    "9080": ("farys", "de_watergroep"),  # Lochristi
    "9550": ("farys", "de_watergroep"),  # Herzele
    "9570": ("farys", "de_watergroep"),  # Lierde
}


def resolve_candidates(postcode: str | None) -> tuple[str, ...]:
    """Return every utility plausibly serving ``postcode``.

    Returns:
        Empty tuple if the postcode is unknown / unresolvable.
        Single-element tuple for unambiguous postcodes.
        Multi-element tuple for postcodes genuinely split between
        operators at street level (see ``_SPLIT_POSTCODES``).
    """
    if not postcode:
        return ()
    try:
        code = int(postcode)
    except (TypeError, ValueError):
        return ()
    key = str(code).zfill(4)
    if key in _SPLIT_POSTCODES:
        return _SPLIT_POSTCODES[key]
    single = _resolve_single(code)
    return (single,) if single else ()


def _resolve_single(code: int) -> str | None:
    """Range / table-based resolution for non-split postcodes."""
    # Brussels.
    if 1000 <= code <= 1299:
        return "vivaqua"
    # Brabant Wallon.
    if 1300 <= code <= 1499:
        return "inbw"
    # Antwerp city core is Water-link; rest of the province is Pidpa. Ring
    # communes (Edegem, Hove, Mortsel, etc.) overlap with Pidpa's wider
    # service area so we default to Pidpa there and let the user override
    # via the manual picker.
    if 2000 <= code <= 2070:
        return "water_link"
    if 2000 <= code <= 2999:
        return "pidpa"
    # Vlaams-Brabant + Halle-Vilvoorde + Limburg.
    if 1500 <= code <= 1999 or 3000 <= code <= 3999:
        return "de_watergroep"
    # Wallonia: trust the ZDE-derived per-postcode table first. If the
    # postcode isn't in it the user is most likely on a régies communales
    # we don't ship -- return None so they hit the manual picker rather
    # than mis-defaulting to SWDE.
    if 4000 <= code <= 7999:
        return _PER_POSTCODE.get(str(code).zfill(4))
    # Flanders carve-outs in West-Vl.
    if code in (8300, 8301):
        return "agso_knokke"
    if code in _AQUADUIN_POSTCODES:
        return "aquaduin"
    # DWG-served pockets scattered inside the otherwise-Farys block
    # (Kortrijk, Harelbeke, Roeselare, Waregem, Ieper, Sint-Niklaas's
    # outer parishes, Eeklo, Maldegem, etc.). AGSO + Aquaduin carve-outs
    # above win first for the few postcodes they overlap.
    if code in _DWG_POSTCODES_FLANDERS:
        return "de_watergroep"
    # Most of West-Vl. + Oost-Vl. is Farys territory; the carve-outs above
    # (AGSO, Aquaduin, DWG) win first.
    if 8000 <= code <= 9999:
        return "farys"
    return None


def resolve(postcode: str | None) -> str | None:
    """Return the dominant utility for ``postcode``, or ``None`` if unknown.

    Convenience wrapper around :func:`resolve_candidates`. For postcodes
    in :data:`_SPLIT_POSTCODES` this returns the first candidate (the
    operator the legacy range rule would have picked); callers that
    need to surface a chooser should use :func:`resolve_candidates`
    directly.
    """
    candidates = resolve_candidates(postcode)
    return candidates[0] if candidates else None
