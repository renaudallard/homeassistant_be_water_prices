"""Postcode → utility resolver.

Two layers of resolution:

  1. Wallonia (postcodes 4000-7999) uses ``_PER_POSTCODE``,
     a 496-entry dict generated by ``scripts/refresh_postcodes.py``
     from the Géoportail Wallonie ZDE GeoPackage (CC BY 4.0,
     dataset id ``d08e993e-bb1d-4322-a044-2aa7bfc03247``). For each
     Walloon postcode polygon the script point-in-polygon-queries
     the ArcGIS REST endpoint to learn which DISTRIBUTEUR serves it,
     then maps the official name to our utility id. Postcodes whose
     distributor is one of the ~30 régies communales we don't ship
     an extractor for are intentionally absent and fall through to
     the manual utility picker. Re-run the refresh script annually.

  2. Brussels + Flanders use range-based rules with hand-curated /
     scrape-derived carve-outs (AGSO Knokke-Heist, the 6 Aquaduin
     Westkust postcodes, and a DWG carve-out for the 121 unambiguous
     DWG-served postcodes scattered inside the 8000-9999 Farys
     block). The DWG carve-out is generated by
     ``scripts/refresh_postcodes.py`` from DWG's and Farys's
     (post-filter) commune dropdowns and includes only postcodes
     that DWG serves and Farys doesn't.

  3. 8 postcodes are genuinely split between two or three operators
     at street level (1770 Liedekerke, 8020 Oostkamp, 8400 Oostende,
     8450 Bredene, 8490 Jabbeke, 9080 Lochristi, 9550 Herzele, 9570
     Lierde). For these, ``resolve_candidates`` returns the full
     candidate tuple and the config flow asks the user to pick. The
     ``resolve`` helper still returns a single "dominant" operator
     (the first candidate) for backwards compatibility.

Coverage:

  * 1000-1299    Brussels-Capital                       → VIVAQUA
  * 1300-1499    Brabant Wallon                         → inBW
  * 1500-1999,
    3000-3999    Vlaams-Brabant + Halle-Vilvoorde
                 + Limburg                              → DE WATERGROEP
  * 2000-2070    Antwerp city core                      → WATER-LINK
  * 2100-2999    Rest of Antwerp province               → PIDPA
                 (Water-link's ring communes -- Edegem,
                  Hove, Mortsel, Schoten, Beveren, etc.
                  -- need the manual picker; their
                  postcodes overlap with Pidpa's wider
                  service area so we default to Pidpa
                  outside the city core)
  * 4000-7999    Per the ZDE-derived
                 ``_PER_POSTCODE`` table       → SWDE / CILE /
                                                           inBW / INASEP /
                                                           AIEC / AIEM /
                                                           CIESAC / IDEN /
                                                           IEG (régies
                                                           absent → manual)
  * 8300, 8301   Knokke-Heist                           → AGSO Knokke-Heist
  * 8430, 8450,
    8620, 8630,
    8660, 8670   Aquaduin Westkust communes             → AQUADUIN
  * 8000-9999 (DWG-served carve-out, 121 postcodes)     → DE WATERGROEP
  * 1770, 8020, 8400,
    8450, 8490, 9080,
    9550, 9570       Real splits (two or three ops)     → multi-choice
  * 8000-9999 (rest, mostly Oost-Vl. and West-Vl.)      → FARYS
  * everything else                                     → unresolved
"""

